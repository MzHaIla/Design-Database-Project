--CUSTOMER
CREATE TABLE Customers(
    Customer_ID         INT,
    Customer_Name       VARCHAR(50)    NOT NULL,
    Customer_Address    VARCHAR(255)    NOT NULL,
    Customer_Contact    VARCHAR(20)    NOT NULL,
    Customer_Email      VARCHAR(255),
    --CONSTRAINT
    CONSTRAINT Customers_PK 
        PRIMARY KEY(Customer_ID),
    CONSTRAINT Customer_Email_CHK 
        CHECK (Customer_Email LIKE '%___@___%.__%')
);

--JOB
CREATE TABLE Jobs(
    Job_ID              INT,
    Job_Name            VARCHAR(50)    NOT NULL,
    Job_Pay_Rate        NUMBER(4, 2)    NOT NULL,
    --CONSTRAINT
    CONSTRAINT Jobs_PK 
        PRIMARY KEY(Job_ID)
);

--PAYMENT_METHOD
CREATE TABLE Payment_Methods(
    Payment_Method_ID   INT,
    Payment_Method_Name VARCHAR(100)    NOT NULL,
    --CONSTRAINT
    CONSTRAINT Payment_Methods_PK 
        PRIMARY KEY(Payment_Method_ID)
);

--SUPPLIER
CREATE TABLE Suppliers(
    Supplier_ID         INT,
    Supplier_Name       VARCHAR(100)    NOT NULL,
    Supplier_Contact    VARCHAR(20)    NOT NULL,
    Supplier_Email      VARCHAR(100)    NOT NULL,
    Supplier_Address    VARCHAR(255)   NOT NULL,
    --CONSTRAINT
    CONSTRAINT Suppliers_PK 
        PRIMARY KEY(Supplier_ID),
    CONSTRAINT Supplier_Email_CHK 
        CHECK (Supplier_Email LIKE '%___@___%.__%')
);

--INGREDIENT
CREATE TABLE Ingredients(
    Ingredient_ID               INT,
    Ingredient_Name             VARCHAR(100)    NOT NULL,
    Ingredient_Quantity_Left    INT             NOT NULL,
    --CONSTRAINT
    CONSTRAINT Ingredients_PK 
        PRIMARY KEY(Ingredient_ID)
);

--MENU
CREATE TABLE Menu(
    Menu_ID             INT,
    Menu_Image          BLOB,
    Menu_Name           VARCHAR(255)   NOT NULL,
    Menu_Status         VARCHAR(2)      NOT NULL,
    Menu_Type           VARCHAR(20)    NOT NULL,
    Menu_Price          NUMBER(4, 2)    NOT NULL,
    --CONSTRAINT
    CONSTRAINT Menu_PK 
        PRIMARY KEY(Menu_ID),
    CONSTRAINT Menu_Status_CHK
        CHECK (Menu_Status IN ('A', 'NA')),
    CONSTRAINT Menu_Price_CHK
        CHECK (Menu_Price > 0)
);

--VOUCHER
CREATE TABLE Vouchers(
    Voucher_Code        VARCHAR(255),
    Voucher_Name        VARCHAR(100)    NOT NULL,
    Voucher_Discount    INT             NOT NULL,
    Voucher_Expiry_Date DATE            NOT NULL,
    Voucher_Valid       CHAR            DEFAULT 'T',
    --CONSTRAINT
    CONSTRAINT Vouchers_PK 
        PRIMARY KEY(Voucher_Code),
    CONSTRAINT Voucher_Valid_CHK 
        CHECK (Voucher_Valid IN ('T', 'F'))
);


--MENU_INGREDIENT
CREATE TABLE Menu_Ingredients(
    Menu_ID             INT,
    Ingredient_ID       INT,
    --CONSTRAINT
    CONSTRAINT Menu_Ingredients_PK 
        PRIMARY KEY(Menu_ID, Ingredient_ID),
    CONSTRAINT MenuIngredient_Menu_FK
        FOREIGN KEY (Menu_ID) 
        REFERENCES Menu(Menu_ID),
    CONSTRAINT Menu_Ingredient_Ingredient_FK 
        FOREIGN KEY (Ingredient_ID) 
        REFERENCES Ingredients(Ingredient_ID)
);

--EMPLOYEE
CREATE TABLE Employees(
    Employee_ID         INT,
    Employee_Name       VARCHAR(50)    NOT NULL,
    Employee_IC         VARCHAR(20)    NOT NULL,
    Employee_Contact    VARCHAR(20)    NOT NULL,
    Employee_Gender     VARCHAR(1)     NOT NULL,   --M/F
    Employee_Email      VARCHAR(30)    NOT NULL,
    Employee_Address    VARCHAR(100),
    Employee_Hire_Date  DATE            NOT NULL,
    Job_ID              INT             NOT NULL,
    Employee_Work_Hours NUMBER(4,2)             NOT NULL,
    --CONSTRAINT
    CONSTRAINT Employees_PK 
        PRIMARY KEY(Employee_ID),
    CONSTRAINT Employee_Job_FK 
        FOREIGN KEY (Job_ID) 
        REFERENCES Jobs(Job_ID),
    CONSTRAINT Employee_Gender_CHK 
        CHECK (Employee_Gender IN ('M', 'F')),
    CONSTRAINT Employee_Email_CHK
        CHECK (Employee_Email LIKE '%___@___%.__%')
);


--DELIVERY
CREATE TABLE Delivery(
    Delivery_ID     INT,
    Employee_ID     INT     NOT NULL,
    --CONSTRAINT
    CONSTRAINT Delivery_PK 
        PRIMARY KEY(Delivery_ID),
    CONSTRAINT Delivery_Employee_FK 
        FOREIGN KEY(Employee_ID) 
        REFERENCES Employees(Employee_ID)
);



--ORDER
CREATE TABLE Orders(
    Order_ID            INT,
    Order_Date_Time     DATE            NOT NULL,
    Customer_ID         INT             NOT NULL,
    Table_Number        INT,
    Delivery_ID         INT,
    Employee_ID         INT             NOT NULL,
    Order_Subtotal      NUMBER(8, 2)    NOT NULL,
    --CONSTRAINT
    CONSTRAINT Orders_PK 
        PRIMARY KEY (Order_ID),
    CONSTRAINT Order_Customer_FK 
        FOREIGN KEY (Customer_ID) 
        REFERENCES Customers(Customer_ID),
    CONSTRAINT Order_Employee_FK 
        FOREIGN KEY (Employee_ID) 
        REFERENCES Employees(Employee_ID),
    CONSTRAINT Order_Delivery_FK
        FOREIGN KEY (Delivery_ID)
        REFERENCES Delivery(Delivery_ID)
);

--ORDER_MENU
CREATE TABLE Order_Menu(
    Order_ID            INT         NOT NULL,
    Menu_ID             INT         NOT NULL,
    Order_Quantity      INT         NOT NULL,
    --CONSTRAINT
    CONSTRAINT Order_Menu_PK 
        PRIMARY KEY(Order_ID, Menu_ID),
    CONSTRAINT OrderMenu_Order_FK 
        FOREIGN KEY(Order_ID) 
        REFERENCES Orders(Order_ID),
    CONSTRAINT OrderMenu_Menu_FK 
        FOREIGN KEY(Menu_ID) 
        REFERENCES Menu(Menu_ID),
    CONSTRAINT Order_Quantity_CHK 
        CHECK (Order_Quantity > 0)
);

--SUPPLY
CREATE TABLE Supply(
    Supplier_ID         INT,
    Ingredient_ID       INT,
    Supply_Quantity     INT             NOT NULL,
    Supply_Total_Price  NUMBER(8, 2)    NOT NULL,
    --CONSTRAINT
    CONSTRAINT Supply_PK 
        PRIMARY KEY (Supplier_ID, Ingredient_ID),
    CONSTRAINT Supply_Supplier_FK 
        FOREIGN KEY (Supplier_ID) 
        REFERENCES Suppliers(Supplier_ID),
    CONSTRAINT Supply_Ingredient_FK 
        FOREIGN KEY (Ingredient_ID)
        REFERENCES Ingredients(Ingredient_ID),
    CONSTRAINT Supply_Quantity_CHK 
        CHECK (Supply_Quantity > 0),
    CONSTRAINT Supply_Total_Price_CHK 
        CHECK (Supply_Total_Price > 0)
);


--PAYMENT
CREATE TABLE Payments(
    Payment_Code        INT,
    Order_ID            INT             NOT NULL,
    Voucher_Code        VARCHAR(255),
    Payment_Date_Time   DATE            NOT NULL,
    Employee_ID         INT             NOT NULL,
    Payment_Method_ID   INT             NOT NULL,
    Payment_Amount      NUMBER(8, 2)    NOT NULL,
    --CONSTRAINT
    CONSTRAINT Payments_PK 
        PRIMARY KEY(Payment_Code),
    CONSTRAINT Payment_Order_FK 
        FOREIGN KEY(Order_ID) 
        REFERENCES Orders(Order_ID),
    CONSTRAINT Payment_Voucher_FK 
        FOREIGN KEY(Voucher_Code) 
        REFERENCES Vouchers(Voucher_Code),
    CONSTRAINT Payment_Employee_FK 
        FOREIGN KEY(Employee_ID) 
        REFERENCES Employees(Employee_ID),
    CONSTRAINT Payment_Method_FK 
        FOREIGN KEY(Payment_Method_ID) 
        REFERENCES Payment_Methods(Payment_Method_ID)
);


